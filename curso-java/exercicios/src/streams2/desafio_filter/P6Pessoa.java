package streams2.desafio_filter;

public class P6Pessoa {

	private String nome;
	private char sexo;
	private int idade;
	
	
	public P6Pessoa(String nome, char sexo, int idade) {
		super();
		this.nome = nome;
		this.sexo = sexo;
		this.idade = idade;
	}
	
	//// ----------------------------------------------------

	public String getNome() {
		return nome;
	}

	public char getSexo() {
		return sexo;
	}

	public int getIdade() {
		return idade;
	}

}
